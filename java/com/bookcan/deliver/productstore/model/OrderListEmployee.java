package com.bookcan.deliver.productstore.model;

public class OrderListEmployee {
    private String product_id;
    private String product_name;
    private String product_img;
    private String final_price;
    private String quantity;
    private String price;
    private String price_type_id;
    private String price_type_name;
    private String product_des;


    // Getter Methods

    public String getProduct_id() {
        return product_id;
    }

    public String getProduct_name() {
        return product_name;
    }

    public String getProduct_img() {
        return product_img;
    }

    public String getFinal_price() {
        return final_price;
    }

    public String getQuantity() {
        return quantity;
    }

    public String getPrice() {
        return price;
    }

    public String getPrice_type_id() {
        return price_type_id;
    }

    public String getPrice_type_name() {
        return price_type_name;
    }

    public String getProduct_des() {
        return product_des;
    }
}
